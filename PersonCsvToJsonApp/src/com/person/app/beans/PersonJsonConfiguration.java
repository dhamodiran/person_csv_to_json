package com.person.app.beans;

public class PersonJsonConfiguration {
	
	String csvAttribute;
	String jsonAttribute;
	String type;
	
	public String getCsvAttribute() {
		return csvAttribute;
	}
	public void setCsvAttribute(String csvAttribute) {
		this.csvAttribute = csvAttribute;
	}
	public String getJsonAttribute() {
		return jsonAttribute;
	}
	public void setJsonAttribute(String jsonAttribute) {
		this.jsonAttribute = jsonAttribute;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
	

}
